'''Given a string - str =“I am a computer science student”
i. Prints the first character of the string.
ii. Prints the character starting from 3rd to 5th.
iii. Prints the character starting from the 3rd character'''

string ="I am a computer science student"

print(f"i. First character: '{string[0]}'")
print(f"ii. Characters from 3rd to 5th: '{string[2:5]}'")
print(f"iii. Characters from 3rd: '{string[2:]}'")